
public class Conditionals {

	public static void main(String[] args) {
		
	
		boolean value1 = true;
		boolean value2 = false;
		
		if(value1) // test for true condition
		{
			System.out.println("Value1 is true");
		}else 
		{
			System.out.println("value1 is false");
		}
		if(value2) 
		{
			System.out.println("Value2 is true");
		}else //test for false value
			System.out.println("value2 is false");
		
	}
}
